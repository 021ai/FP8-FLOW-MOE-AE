"""
Plot speedup comparison for scaling-aware FP8 transpose operator.

Reads benchmark results from CSV (generated by bench.py) or xlsx (legacy),
and produces one bar chart:
  1. perf_scaling_aware_transpose.pdf — scaling-aware-transpose vs naive (DoubleQuant)

Usage:
    python plot_perf.py
"""

import os

import numpy as np
import pandas as pd
import matplotlib.pyplot as plt


# ---------------------------------------------------------------------------
# Data loading
# ---------------------------------------------------------------------------
EXPECTED_OUTPUT_DIR = "expected_output"
CURRENT_CSV_PATH = "perf_data.csv"
EXPECTED_CSV_PATH = f"{EXPECTED_OUTPUT_DIR}/perf_data_expected.csv"
print(f'{EXPECTED_CSV_PATH}')
if os.path.exists(CURRENT_CSV_PATH):
    data = pd.read_csv(CURRENT_CSV_PATH)
    print("read current")
elif os.path.exists(EXPECTED_CSV_PATH):
    data = pd.read_csv(EXPECTED_CSV_PATH)
    print("read legacy csv")
else:
    raise FileNotFoundError(
        f"No data file found: tried {CURRENT_CSV_PATH}, {EXPECTED_CSV_PATH}"
    )

# ---------------------------------------------------------------------------
# Parse config column: "(M,N)"
# ---------------------------------------------------------------------------
parsed = (
    data.iloc[:, 0]
    .str.replace("(", "", regex=False)
    .str.replace(")", "", regex=False)
    .str.split(",", expand=True)
)

M = parsed[0].astype(int)
N = parsed[1].astype(int)

# X-axis labels: (M, N)
labels = "(" + M.astype(str) + "," + N.astype(str) + ")"

# ---------------------------------------------------------------------------
# Extract speedup column (col index: 3)
# ---------------------------------------------------------------------------
speedup = data.iloc[:, 3].astype(float)

# Naive baseline is always 1.0
naive = np.ones_like(speedup)

# ---------------------------------------------------------------------------
# Plot settings
# ---------------------------------------------------------------------------
x = np.arange(len(labels))
BAR_WIDTH = 0.20

plt.rcParams.update({
    "font.size": 30,
    "axes.titlesize": 30,
    "axes.labelsize": 30,
    "xtick.labelsize": 28,
    "ytick.labelsize": 30,
    "legend.fontsize": 30,
    "figure.titlesize": 32,
})


def plot(naive, speedup, output_path, output_type, kernel_name):
    """Generate a grouped bar chart comparing naive vs fused kernel speedup.

    Args:
        naive: Array of baseline values (all ones).
        speedup: Speedup of fused kernel vs naive.
        output_path: Output file path (without extension).
        output_type: 1 for PNG, 2 for PDF.
        kernel_name: Name of the fused kernel (used in legend).
    """
    fig, ax = plt.subplots(figsize=(20, 10), dpi=600)

    # Two grouped bars per x-tick
    ax.bar(x - 0.5 * BAR_WIDTH, naive, BAR_WIDTH,
           label="naive (DoubleQuant)", color="#F27970", edgecolor="black")
    ax.bar(x + 0.5 * BAR_WIDTH, speedup, BAR_WIDTH,
           label=kernel_name, color="#8983BF", edgecolor="black")

    # Annotate values above each bar
    for i in range(len(x)):
        ax.text(x[i] - 0.5 * BAR_WIDTH, naive[i] + 0.02,
                f"{naive[i]:.2f}", ha="center", va="bottom", fontsize=15)
        ax.text(x[i] + 0.5 * BAR_WIDTH, speedup.iloc[i] + 0.02,
                f"{speedup.iloc[i]:.2f}", ha="center", va="bottom", fontsize=15)

    ax.set_xticks(x)
    ax.set_xticklabels(labels)
    ax.set_ylabel("Speedup (vs. naive)")
    ax.set_xlabel("Input Shape in (M, N)")
    ax.set_ylim(0, max(max(speedup), max(naive)) * 1.3)
    ax.grid(axis="y", alpha=0.25)
    ax.legend(ncol=2, loc="upper left")

    fig.tight_layout()
    if output_type == 1:
        fig.savefig(f"{output_path}.png", dpi=200, bbox_inches="tight")
    elif output_type == 2:
        fig.savefig(f"{output_path}.pdf", format="pdf", bbox_inches="tight")
    plt.show()


# ---------------------------------------------------------------------------
# Generate plots
# ---------------------------------------------------------------------------
plot(
    naive, speedup,
    f"{EXPECTED_OUTPUT_DIR}/perf_scaling_aware_transpose", 2, "scaling-aware-transpose",
)
