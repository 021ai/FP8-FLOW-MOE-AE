"""
Plot speedup comparison for fused weighted-swiglu + quantization operator.

Reads benchmark results from CSV (generated by bench.py),
and produces one bar chart:
  1. perf_swiglu.pdf — fwd/bwd speedup of fused_swiglu_quant vs naive

Usage:
    python plot_perf.py
"""

import os

import numpy as np
import pandas as pd
import matplotlib.pyplot as plt


# ---------------------------------------------------------------------------
# Data loading
# ---------------------------------------------------------------------------
EXPECTED_OUTPUT_DIR = "expected_output"
CURRENT_CSV_PATH = "perf_data.csv"
EXPECTED_CSV_PATH = f"{EXPECTED_OUTPUT_DIR}/perf_data_expected.csv"

if os.path.exists(CURRENT_CSV_PATH):
    data = pd.read_csv(CURRENT_CSV_PATH)
    print(f'read current')
elif os.path.exists(EXPECTED_CSV_PATH):
    data = pd.read_csv(EXPECTED_CSV_PATH)
    print(f'read legacy')
else:
    raise FileNotFoundError(f"No data file found: tried {CURRENT_CSV_PATH} and {EXPECTED_CSV_PATH}")

# ---------------------------------------------------------------------------
# Parse config column: "(num_tokens, hidden_size, num_expert, topK, MOE_INTERMEDIATE_SIZE)"
# ---------------------------------------------------------------------------
parsed = (
    data.iloc[:, 0]
    .str.replace("(", "", regex=False)
    .str.replace(")", "", regex=False)
    .str.split(",", expand=True)
)

num_tokens = parsed[0].astype(int)
hidden_size = parsed[1].astype(int)
num_experts = parsed[2].astype(int)
topK = parsed[3].astype(int)
moe_intermediate_size = parsed[4].astype(int)

# X-axis labels: (num_tokens * topK, 2 * MOE_INTERMEDIATE_SIZE)
labels = "(" + (num_tokens * topK).astype(str) + "," + (2 * moe_intermediate_size).astype(str) + ")"

# ---------------------------------------------------------------------------
# Extract speedup columns (col indices: 3, 6)
# ---------------------------------------------------------------------------
fwd_speedup = data.iloc[:, 3].astype(float)
bwd_speedup = data.iloc[:, 6].astype(float)

# Naive baseline is always 1.0
naive = np.ones_like(fwd_speedup)

# ---------------------------------------------------------------------------
# Plot settings
# ---------------------------------------------------------------------------
x = np.arange(len(labels))
BAR_WIDTH = 0.20

plt.rcParams.update({
    "font.size": 30,
    "axes.titlesize": 30,
    "axes.labelsize": 30,
    "xtick.labelsize": 28,
    "ytick.labelsize": 30,
    "legend.fontsize": 30,
    "figure.titlesize": 32,
})


def plot(naive, fwd_speedup, bwd_speedup, output_path, output_type, kernel_name):
    """Generate a grouped bar chart comparing naive vs fused kernel speedup.

    Args:
        naive: Array of baseline values (all ones).
        fwd_speedup: Forward-pass speedup of fused kernel.
        bwd_speedup: Backward-pass speedup of fused kernel.
        output_path: Output file path (without extension).
        output_type: 1 for PNG, 2 for PDF.
        kernel_name: Name of the fused kernel (used in legend).
    """
    fig, ax = plt.subplots(figsize=(20, 10), dpi=600)

    # Four grouped bars per x-tick
    ax.bar(x - 1.5 * BAR_WIDTH, naive, BAR_WIDTH,
           label="fwd naive", color="#F27970", edgecolor="black")
    ax.bar(x - 0.5 * BAR_WIDTH, fwd_speedup, BAR_WIDTH,
           label=f"fwd {kernel_name}", edgecolor="#F27970",
           fill=False, hatch="/////", alpha=0.8)
    ax.bar(x + 0.5 * BAR_WIDTH, naive, BAR_WIDTH,
           label="bwd naive", color="#8983BF", edgecolor="black")
    ax.bar(x + 1.5 * BAR_WIDTH, bwd_speedup, BAR_WIDTH,
           label=f"bwd {kernel_name}", edgecolor="#8983BF",
           fill=False, hatch="/////", alpha=0.8)

    # Annotate values above each bar
    for i in range(len(x)):
        ax.text(x[i] - 1.5 * BAR_WIDTH, naive[i] + 0.02,
                f"{naive[i]:.2f}", ha="center", va="bottom", fontsize=15)
        ax.text(x[i] - 0.5 * BAR_WIDTH, fwd_speedup.iloc[i] + 0.02,
                f"{fwd_speedup.iloc[i]:.2f}", ha="center", va="bottom", fontsize=15)
        ax.text(x[i] + 0.5 * BAR_WIDTH, naive[i] + 0.02,
                f"{naive[i]:.2f}", ha="center", va="bottom", fontsize=15)
        ax.text(x[i] + 1.5 * BAR_WIDTH, bwd_speedup.iloc[i] + 0.02,
                f"{bwd_speedup.iloc[i]:.2f}", ha="center", va="bottom", fontsize=15)

    ax.set_xticks(x)
    ax.set_xticklabels(labels)
    ax.set_ylabel("Speedup (vs. naive)")
    ax.set_xlabel("Input Shape in (M, N)")
    ax.set_ylim(0, max(max(fwd_speedup), max(bwd_speedup)) * 1.3)
    ax.grid(axis="y", alpha=0.25)
    ax.legend(ncol=2, loc="upper left")

    fig.tight_layout()
    if output_type == 1:
        fig.savefig(f"{output_path}.png", dpi=200, bbox_inches="tight")
    elif output_type == 2:
        fig.savefig(f"{output_path}.pdf", format="pdf", bbox_inches="tight")
    plt.show()


# ---------------------------------------------------------------------------
# Generate plots
# ---------------------------------------------------------------------------
plot(
    naive, fwd_speedup, bwd_speedup,
    f"{EXPECTED_OUTPUT_DIR}/perf_swiglu", 2, "fused_swiglu_quant",
)
