"""
Plot speedup comparison for fused permute+pad and unpermute+unpad operators.

Reads benchmark results from CSV (generated by bench.py),
and produces two bar charts:
  1. perf_perm_pad.pdf     — permute + padding speedup
  2. perf_unperm_unpad.pdf — unpermute + unpadding speedup

Usage:
    python plot_perf.py
"""

import os

import numpy as np
import pandas as pd
import matplotlib.pyplot as plt


# ---------------------------------------------------------------------------
# Data loading
# ---------------------------------------------------------------------------
EXPECTED_OUTPUT_DIR = "expected_output"
CURRENT_CSV_PATH = "perf_data.csv"
EXPECTED_CSV_PATH = f"{EXPECTED_OUTPUT_DIR}/perf_data_expected.csv"

if os.path.exists(CURRENT_CSV_PATH):
    data = pd.read_csv(CURRENT_CSV_PATH)
    print(f'read current')
elif os.path.exists(EXPECTED_CSV_PATH):
    data = pd.read_csv(EXPECTED_CSV_PATH)
    print(f'read legacy')
else:
    raise FileNotFoundError(f"No data file found: tried {CURRENT_CSV_PATH} and {EXPECTED_CSV_PATH}")

# ---------------------------------------------------------------------------
# Parse config column: "(num_tokens, num_experts, hidden_size, topK)"
# ---------------------------------------------------------------------------
parsed = (
    data.iloc[:, 0]
    .str.replace("(", "", regex=False)
    .str.replace(")", "", regex=False)
    .str.split(",", expand=True)
)

num_tokens = parsed[0].astype(int)
num_experts = parsed[1].astype(int)
hidden_size = parsed[2].astype(int)
topK = parsed[3].astype(int)

# X-axis labels: (num_tokens * topK, hidden_size)
labels = "(" + (num_tokens * topK).astype(str) + "," + hidden_size.astype(str) + ")"

# ---------------------------------------------------------------------------
# Extract speedup columns (col indices: 3, 6, 9, 12)
# ---------------------------------------------------------------------------
# bfloat16
bf16_speedup_perm_pad = data.iloc[:, 3].astype(float)
bf16_speedup_unperm_unpad = data.iloc[:, 6].astype(float)
# FP8 (Float8BlockwiseQTensor)
fp8_speedup_perm_pad = data.iloc[:, 9].astype(float)
fp8_speedup_unperm_unpad = data.iloc[:, 12].astype(float)

# Naive baseline is always 1.0
naive_perm_pad = np.ones_like(bf16_speedup_perm_pad)
naive_unperm_unpad = np.ones_like(bf16_speedup_unperm_unpad)

# ---------------------------------------------------------------------------
# Plot settings
# ---------------------------------------------------------------------------
x = np.arange(len(labels))
BAR_WIDTH = 0.20

plt.rcParams.update({
    "font.size": 30,
    "axes.titlesize": 30,
    "axes.labelsize": 30,
    "xtick.labelsize": 28,
    "ytick.labelsize": 30,
    "legend.fontsize": 30,
    "figure.titlesize": 32,
})


def plot(naive, bf16_speedup, fp8_speedup, output_path, output_type, kernel_name):
    """Generate a grouped bar chart comparing naive vs fused kernel speedup.

    Args:
        naive: Array of baseline values (all ones).
        bf16_speedup: Speedup of fused kernel under bfloat16.
        fp8_speedup: Speedup of fused kernel under FP8 blockwise quantization.
        output_path: Output file path (without extension).
        output_type: 1 for PNG, 2 for PDF.
        kernel_name: Name of the fused kernel (used in legend).
    """
    fig, ax = plt.subplots(figsize=(20, 10), dpi=600)

    # Four grouped bars per x-tick
    ax.bar(x - 1.5 * BAR_WIDTH, naive, BAR_WIDTH,
           label="bf16 naive", color="#F27970", edgecolor="black")
    ax.bar(x - 0.5 * BAR_WIDTH, bf16_speedup, BAR_WIDTH,
           label=f"bf16 {kernel_name}", edgecolor="#F27970",
           fill=False, hatch="/////", alpha=0.8)
    ax.bar(x + 0.5 * BAR_WIDTH, naive, BAR_WIDTH,
           label="fp8 naive", color="#8983BF", edgecolor="black")
    ax.bar(x + 1.5 * BAR_WIDTH, fp8_speedup, BAR_WIDTH,
           label=f"fp8 {kernel_name}", edgecolor="#8983BF",
           fill=False, hatch="/////", alpha=0.8)

    # Annotate values above each bar
    for i in range(len(x)):
        ax.text(x[i] - 1.5 * BAR_WIDTH, naive[i] + 0.02,
                f"{naive[i]:.2f}", ha="center", va="bottom", fontsize=15)
        ax.text(x[i] - 0.5 * BAR_WIDTH, bf16_speedup.iloc[i] + 0.02,
                f"{bf16_speedup.iloc[i]:.2f}", ha="center", va="bottom", fontsize=15)
        ax.text(x[i] + 0.5 * BAR_WIDTH, naive[i] + 0.02,
                f"{naive[i]:.2f}", ha="center", va="bottom", fontsize=15)
        ax.text(x[i] + 1.5 * BAR_WIDTH, fp8_speedup.iloc[i] + 0.02,
                f"{fp8_speedup.iloc[i]:.2f}", ha="center", va="bottom", fontsize=15)

    ax.set_xticks(x)
    ax.set_xticklabels(labels)
    ax.set_ylabel("Speedup (vs. naive)")
    ax.set_xlabel("Input Shape in (M, N)")
    ax.set_ylim(0, max(max(bf16_speedup), max(fp8_speedup)) * 1.3)
    ax.grid(axis="y", alpha=0.25)
    ax.legend(ncol=2, loc="upper left")

    fig.tight_layout()
    if output_type == 1:
        fig.savefig(f"{output_path}.png", dpi=200, bbox_inches="tight")
    elif output_type == 2:
        fig.savefig(f"{output_path}.pdf", format="pdf", bbox_inches="tight")
    plt.show()


# ---------------------------------------------------------------------------
# Generate plots
# ---------------------------------------------------------------------------
plot(
    naive_perm_pad, bf16_speedup_perm_pad, fp8_speedup_perm_pad,
    f"{EXPECTED_OUTPUT_DIR}/perf_perm_pad", 2, "fused_permute_pad",
)

plot(
    naive_unperm_unpad, bf16_speedup_unperm_unpad, fp8_speedup_unperm_unpad,
    f"{EXPECTED_OUTPUT_DIR}/perf_unperm_unpad", 2, "fuse_unpermute_unpad",
)
